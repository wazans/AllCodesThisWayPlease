package Class1_OOPS;

public class Class1_topic14_ConversionAndCasting {
    public static void main(String[] args) {
        int a = 10;
        double b = a;
        System.out.println(b);

        double x = 10.75;
        int y = (int) x;
        System.out.println(y);
    }
}

/*
OUTPUT:
10.0
10
*/
